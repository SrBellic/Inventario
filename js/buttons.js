function expandirBoton() {
    document.querySelector('.boton').style.width = '150px';
    document.querySelector('.boton').style.height = '50px';
    document.querySelector('.boton').style.borderRadius = '25px';
  }
  
  function contraerBoton() {
    document.querySelector('.boton').style.width = '50px';
    document.querySelector('.boton').style.height = '50px';
    document.querySelector('.boton').style.borderRadius = '50%';
  }
  